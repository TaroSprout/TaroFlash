INSERT INTO public.decks (id, title, is_public) VALUES (303, 'Mixed: steps overridden, retention from system', false);
INSERT INTO public.deck_review_pacing (deck_id, overrides)
VALUES (303, '{"learning_steps": ["30m"], "relearning_steps": ["1h"]}'::jsonb);

