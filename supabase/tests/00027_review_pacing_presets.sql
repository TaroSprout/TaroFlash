-- ── Act as Alice — RLS SELECT ─────────────────────────────────────────────────

SET LOCAL role = 'postgres';
SELECT tests.set_claims('a1111111-1111-1111-1111-111111111111'::uuid);
SET LOCAL role = 'authenticated';

