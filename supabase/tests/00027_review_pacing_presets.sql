-- Test 14 [obligation]: deck 301 relearning_steps resolves to the linked preset.
SELECT is(
  (SELECT relearning_steps FROM public.get_member_decks(date_trunc('day', now())) WHERE id = 301),
  ARRAY['1d'],
  'deck 301 (empty overrides, linked preset) relearning_steps resolves to the preset [obligation]'
