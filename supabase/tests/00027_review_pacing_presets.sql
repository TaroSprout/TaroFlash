-- ── Daily-limit resolution ladder [obligation] ────────────────────────────────
-- Distinguishes "no preset linked" (falls through to the system default)
-- from "preset linked but its own max_*_per_day is NULL" (that preset's
-- explicit "unbounded" — must NOT fall through to the system default).

-- Deck 306: no preset, no override -> system default.
INSERT INTO public.decks (id, title, is_public) VALUES (306, 'No preset - system default', false);

-- Deck 307: linked to preset 9004 (max_reviews_per_day = 40, max_new_per_day
-- = NULL, max_interval = 60). max_reviews_per_day must resolve to 40 (the
-- preset's value), and max_new_per_day must resolve to NULL (the preset's
-- own "unbounded") — NOT fall through to the system preset's value.
INSERT INTO public.decks (id, title, is_public) VALUES (307, 'Preset with NULL max_new_per_day', false);
INSERT INTO public.deck_review_pacing (deck_id, review_pacing_preset_id) VALUES (307, 9004);

-- Test 24: deck 306 (no preset linked) resolves max_reviews_per_day to the
-- system preset's value, proving the "no preset" branch still falls through.
SELECT is(
  (SELECT max_reviews_per_day FROM public.get_member_decks(date_trunc('day', now())) WHERE id = 306),
  (SELECT max_reviews_per_day FROM public.review_pacing_presets WHERE is_system),
  'deck 306 (no preset) resolves max_reviews_per_day to the system preset [obligation]'
);

-- Test 25: deck 307 max_reviews_per_day resolves to the linked preset's
-- concrete value (40), not the system default.
SELECT is(
  (SELECT max_reviews_per_day FROM public.get_member_decks(date_trunc('day', now())) WHERE id = 307),
  40,
  'deck 307 max_reviews_per_day resolves to the linked preset''s concrete cap [obligation]'
);

-- Test 26 [obligation]: deck 307 max_new_per_day resolves to NULL — the
-- linked preset's own "unbounded" — and must NOT fall through to the system
-- preset's max_new_per_day (a naive COALESCE would get this wrong).
SELECT is(
  (SELECT max_new_per_day FROM public.get_member_decks(date_trunc('day', now())) WHERE id = 307),
  NULL::int,
  'deck 307 max_new_per_day resolves to the linked preset''s own NULL (unbounded), not the system default [obligation]'
);

-- Test 27 [obligation]: a pinned-null max_interval override wins over the
-- linked preset's own numeric cap (60) — key present with a null value
-- means "pinned to uncapped", resolving to NULL rather than the preset's
-- concrete value.
INSERT INTO public.decks (id, title, is_public) VALUES (308, 'Pinned-null max_interval over a capped preset', false);
INSERT INTO public.deck_review_pacing (deck_id, review_pacing_preset_id, overrides)
VALUES (308, 9004, '{"max_interval": null}'::jsonb);

SELECT is(
  (SELECT max_interval FROM public.get_member_decks(date_trunc('day', now())) WHERE id = 308),
  NULL::int,
  'a pinned-null max_interval override resolves to NULL even when the linked preset has a numeric cap [obligation]'
);

-- Test 28: the same deck's max_reviews_per_day (not pinned) still resolves
-- to the linked preset's concrete value — proving the pin is scoped to the
-- one field, not the whole deck.
SELECT is(
  (SELECT max_reviews_per_day FROM public.get_member_decks(date_trunc('day', now())) WHERE id = 308),
  40,
  'deck 308 max_reviews_per_day (unpinned) still resolves to the linked preset'
);


